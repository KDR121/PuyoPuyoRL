def naive_popcount(n):
    count = 0
    while n:
        if n & 1:
            count += 1
        n >>= 1
    return count


def popcount(a):
    if not isinstance(a, bytearray):
        raise TypeError("Only bytearrays supported")
    return sum(POPCOUNTS_256[b] for b in a)


def bits_to_string(n):
    if not n:
        return "0"
    result = ""
    while n:
        if n & 1:
            result += "1"
        else:
            result += "0"
        n >>= 1
    return result
